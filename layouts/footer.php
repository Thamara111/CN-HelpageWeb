<script>
    $(document).ready(function () {
        let currentStep = 0;
        const steps = $(".form-step");

        function showStep(index) {
            steps.addClass("hidden").removeClass("active");
            steps.eq(index).removeClass("hidden").addClass("active");
        }

        $(".next-btn").click(function () {
            if (currentStep < steps.length - 1) {
                currentStep++;
                showStep(currentStep);
            }
        });

        $(".prev-btn").click(function () {
            if (currentStep > 0) {
                currentStep--;
                showStep(currentStep);
            }
        });

        $('input[name="donation_amount"]').change(function () {
            if ($(this).val() === 'custom') {
                $('#customAmountContainer').slideDown(300);
                $('#customAmount').focus();
            } else {
                $('#customAmountContainer').slideUp(300);
            }
        });

        $('#donationForm').submit(function (e) {
            e.preventDefault();
            let amount = $('input[name="donation_amount"]:checked').val();
            if (amount === 'custom') amount = $('#customAmount').val();
            $('<div class="fixed top-4 right-4 bg-green-500 text-white px-6 py-3 rounded-lg shadow-lg">Donation submitted! Amount: Rs. ' + amount + '</div>').appendTo('body').delay(3000).fadeOut(function () { $(this).remove(); });
        });
    });
</script>
