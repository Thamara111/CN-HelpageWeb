<script>
    $(document).ready(function () {
        const $carousel = $('#hero-carousel');
        const $slides = $carousel.find('[data-carousel-item]');
        const $indicators = $carousel.find('.indicator');
        let currentIndex = 0;
        let interval = null;
        let isTransitioning = false;

        function showSlide(index) {
            if (isTransitioning || index === currentIndex) return;

            isTransitioning = true;

            // Fade out current slide
            $slides.eq(currentIndex).fadeOut(600, function () {
                // Fade in new slide
                $slides.eq(index).fadeIn(600, function () {
                    isTransitioning = false;
                });

                // Update indicators
                $indicators.removeClass('bg-white').addClass('bg-white/50');
                $indicators.eq(index).removeClass('bg-white/50').addClass('bg-white');

                currentIndex = index;
            });
        }

        function nextSlide() {
            let newIndex = (currentIndex + 1) % $slides.length;
            showSlide(newIndex);
        }

        function prevSlide() {
            let newIndex = (currentIndex - 1 + $slides.length) % $slides.length;
            showSlide(newIndex);
        }

        function resetInterval() {
            clearInterval(interval);
            interval = setInterval(nextSlide, 5000);
        }

        // Initialize
        $slides.hide();
        $slides.eq(0).show();
        $indicators.eq(0).removeClass('bg-white/50').addClass('bg-white');

        // Start auto-slide
        interval = setInterval(nextSlide, 5000);

        // Event handlers
        $carousel.find('[data-carousel-next]').on('click', function (e) {
            e.preventDefault();
            nextSlide();
            resetInterval();
        });

        $carousel.find('[data-carousel-prev]').on('click', function (e) {
            e.preventDefault();
            prevSlide();
            resetInterval();
        });

        $indicators.on('click', function (e) {
            e.preventDefault();
            const index = $(this).data('slide-to');
            showSlide(index);
            resetInterval();
        });

        // Pause on hover
        $carousel.hover(
            function () {
                clearInterval(interval);
            },
            function () {
                resetInterval();
            }
        );

        // Keyboard navigation
        $(document).on('keydown', function (e) {
            if (e.key === 'ArrowLeft') {
                prevSlide();
                resetInterval();
            } else if (e.key === 'ArrowRight') {
                nextSlide();
                resetInterval();
            }
        });
    });
</script>
